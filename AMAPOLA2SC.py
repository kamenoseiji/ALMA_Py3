#---- AMAPOLA2SC.py : Update polarization data in the Source Catalogue (SC)
# Description:
#  This script reads AMAPOLA results of the specified ExecBlock UID and update 'angle', 'angleUncertainty', 'degree', 'degreeUncertainty' entries in the SC.
#  Other items are preserved.
# 
# Usage:
#  python AMAPOLA2SC.py -u [ExecBlock UID]
#  e.g. : python SC_fetchUID.py -u uid://A002/X13cdcf8/X12478
#
# Requirements:
#  + Base64 Credential of username and password must be set as 'b64credentials' environmental variable.
#  + ROLE_SOURCECAT_ADMIN privileges must be given, in advance. See https://confluence.alma.cl/x/G2BiBQ
#
# Notes:
#  + AMAPOLA_URI is planned to move to ICT control in the year 2027.
#
